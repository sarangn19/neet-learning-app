// Helper function to get random questions by subject
export function getRandomQuestions(subject: 'physics' | 'chemistry' | 'biology' | 'mixed', count: number = 5): BattleQuestion[] {
  let filtered = battleQuestions;
  
  if (subject !== 'mixed') {
    filtered = battleQuestions.filter(q => q.subject === subject);
  }
  
  // Shuffle and take required count
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Helper function to get questions by difficulty
export function getQuestionsByDifficulty(difficulty: 'easy' | 'medium' | 'hard', count: number = 5): BattleQuestion[] {
  const filtered = battleQuestions.filter(q => q.difficulty === difficulty);
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
